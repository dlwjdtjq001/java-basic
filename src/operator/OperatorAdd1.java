package operator;

public class OperatorAdd1 {
    public static void main(String[] args) {


        int a = 0; //선언

        //연산
        a = a + 1;
        System.out.println("a = " + a);
        a = a + 1;
        System.out.println("a = " + a);

        //증감연산자
        a += 1;
        System.out.println("a = " + a);
        a += 1;
        System.out.println("a = " + a);

    }
}
